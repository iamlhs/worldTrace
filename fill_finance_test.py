def build_table(tbl_xml, case):
    new_tbl = deepcopy(tbl_xml)
    rows = new_tbl.findall(qn('w:tr'))

    def rc(row_idx):
        return rows[row_idx].findall(qn('w:tc'))

    # Rows 0-5: cell[0]=label, cell[1]=content
    set_cell(rc(0)[1], '金融舆情—' + case['name'] + ' 功能测试')
    set_cell(rc(1)[1], case['gn'])
    set_cell(rc(2)[1], case['desc'])
    set_cell(rc(3)[1], case['indicator'] + '——' + case['module'])
    set_cell(rc(4)[1], case['premise'])
    set_cell(rc(5)[1], '所有测试步骤执行完毕而终止，或因软件运行错误终止')

    # Row 6: 整行合并
    set_cell(rc(6)[0], '测试过程')

    # Rows 8-13: cell[0]=序号, cell[1]=输入(span2), cell[2]=期望, cell[3]=评估, cell[4]=判定
    for si in range(6):
        ri = 8 + si
        c = rc(ri)
        if si < len(case['steps']):
            inp, exp, crit = case['steps'][si]
            set_cell(c[0], str(si + 1))
            set_cell(c[1], inp)
            set_cell(c[2], exp)
            set_cell(c[3], crit)
            set_cell(c[4], '')
        else:
            for ci in range(len(c)):
                set_cell(c[ci], '')

    # Row 14: 备注
    set_cell(rc(14)[1], case['remark'])

    # Row 15: 结论
    set_cell(rc(15)[1], '  □ 通过      □ 未通过     □ 未测试')

    return new_tbl


for idx, case in enumerate(cases):
    new_tbl = build_table(template_tbl_xml, case)
    doc.element.body.append(new_tbl)
    p = OxmlElement('w:p')
    r = OxmlElement('w:r')
    br = OxmlElement('w:br')
    br.set(qn('w:type'), 'page')
    r.append(br)
    p.append(r)
    doc.element.body.append(p)
    print('Added: ' + case['gn'])

doc.save(DOC_PATH)
print('Done!')
